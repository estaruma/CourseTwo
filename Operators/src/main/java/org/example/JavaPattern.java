package org.example;

public class JavaPattern {
    public static void main(String[] args) {
        System.out.println("    J    " + "    A    " + "    V   " + "     V   " + "    A   ");
        System.out.println("    J    " + "   A A   " + "    V V  " + "    V V  " + "   A A  ");
        System.out.println("J   J    " + "  AAAAA  " + "    V V  " + "   V   V " + "  AAAAA ");
        System.out.println(" J J     " + " A     A " + "     V   " + "   V   V " + " A     A");
    }
}